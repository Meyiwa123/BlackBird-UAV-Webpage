	<h1>National Student UAS Competition</h1>
	<p>
		The National Student UAS Competition Student UAS Competition is an annual event hosted by the
		Aerial Evolution Association of Canada (AEAC) that showcases the innovative prowess of students
		in the field of unmanned aerial systems (UAS), commonly known as drones. The competition serves
		as a platform for university teams to demonstrate their technical expertise, problem-solving
		skills, and creative solutions in the realm of unmanned aircraft.
	</p>
	<p>
		Participating teams take on the challenge of designing, constructing, and operating autonomous
		aerial vehicles to address specific mission objectives set by the competition committee. These
		missions often involve solving real-world challenges, such as environmental monitoring, disaster
		response, urban mobility, and more.
	</p>
	<p>
		The competition is divided into phases, each focusing on different aspects of UAS development.
		Phase 1 usually centers around presenting a comprehensive design paper that outlines the team's
		approach, technologies, and strategies. Phase 2, known as the Flying Phase, is the exciting
		culmination where teams gather to showcase their UAS's actual flying capabilities through
		in-person demonstrations.
	</p>
